async def translate_batch(texts: list[str], session: aiohttp.ClientSession) -> list[str]:
    """ë°°ì¹˜ë¡œ ë²ˆì—­ (í•œë²ˆì— ì—¬ëŸ¬ ë¬¸ì¥)"""
    results = []
    
    for text in texts:
        try:
            params = {"q": text[:500], "langpair": "en|ko"}
            async with session.get(
                "https://api.mymemory.translated.net/get",
                params=params,
                timeout=aiohttp.ClientTimeout(total=5)
            ) as response:
                if response.status == 200:
                    data = await response.json()
                    translated = data.get("responseData", {}).get("translatedText", text)
                    results.append(translated)
                else:
                    results.append(text)
        except Exception:
            results.append(text)
        
        await asyncio.sleep(0.1)  # Rate limit
    
    return results


async def translate_all_words(words: list[dict]) -> list[dict]:
    """ëª¨ë“  ë‹¨ì–´ ë²ˆì—­"""
    print(f"ğŸŒ {len(words)}ê°œ ë‹¨ì–´ ë²ˆì—­ ì¤‘...")
    
    connector = aiohttp.TCPConnector(limit=10)
    async with aiohttp.ClientSession(connector=connector) as session:
        # ì •ì˜ ë²ˆì—­
        definitions = [w["definition"] for w in words]
        examples = [w["example"] for w in words]
        
        # ë°°ì¹˜ë¡œ ë‚˜ëˆ ì„œ ë²ˆì—­
        batch_size = 50
        translated_defs = []
        translated_exs = []
        
        for i in range(0, len(definitions), batch_size):
            batch_defs = definitions[i:i+batch_size]
            batch_exs = examples[i:i+batch_size]
            
            print(f"  ë²ˆì—­ ì¤‘: {i+1}-{min(i+batch_size, len(definitions))}/{len(definitions)}")
            
            trans_defs = await translate_batch(batch_defs, session)
            trans_exs = await translate_batch(batch_exs, session)
            
            translated_defs.extend(trans_defs)
            translated_exs.extend(trans_exs)
        
        # ê²°ê³¼ ì ìš©
        for i, word in enumerate(words):
            word["definitionKr"] = translated_defs[i] if i < len(translated_defs) else word["definition"]
            word["exampleKr"] = translated_exs[i] if i < len(translated_exs) else word["example"]
    
    return words


